/**
* @brief This function handles TIM6 global interrupt, DAC1 and DAC2 underrun error interrupts.
*/
void TIM6_DAC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM6_DAC_IRQn 0 */
  spi_timer++;
  
  if(spi_timer == SPI_PERIOD_COUNT){
    ///////////////////////////////////////////////AD7606 DATAREAD///////////////////////////////////////////
    AD7606_DataReadSignal();
    HAL_SPI_Receive(&hspi1, spiData, 16, 15);
    AD7606_DataRead(&hspi1,spiData,16,15,spivalue);
    ///////////////////////////////////////////////AD7606 DATAREAD END///////////////////////////////////////

    ///////////////////////////////////////////////SAMPLING & SPI DATA UART//////////////////////////////////
    for(int i=0; i<8; i++){
      filter_spivalue[i] = LPF_FILTER_TAU*spivalue[i] + (1-LPF_FILTER_TAU)*prev_spivalue[i];
      prev_spivalue[i] = filter_spivalue[i];
    }
    ///////////////////////////////////////////////SAMPLING & SPI DATA UART END/////////////////////////////

    if(spi_sampling_flag == 1)
      SamplingFunc();

    spi_timer = 0;
  }
  /* USER CODE END TIM6_DAC_IRQn 0 */
  HAL_TIM_IRQHandler(&htim6);
  /* USER CODE BEGIN TIM6_DAC_IRQn 1 */

  /* USER CODE END TIM6_DAC_IRQn 1 */
}

/* USER CODE BEGIN 1 */
void SamplingFunc(void){
  if(spi_sampling_cnt < SPI_SAMPLING_COUNT){
    for(int i = 0; i < 8; i++)
      spi_sampling_arr[i] += filter_spivalue[i];
      
    spi_sampling_cnt++;
      
    if(spi_sampling_cnt == SPI_SAMPLING_COUNT){
      spi_sampling_flag = 0;
      for(int i = 0; i < 8; i++)
        spi_sampling_arr[i] = spi_sampling_arr[i] / SPI_SAMPLING_COUNT;
    }
  }
}
